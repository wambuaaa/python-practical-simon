# integer.py
# Demonstrates integer variables and arithmetic operations

# Creating integer variables
a = 15
b = 4

# Arithmetic operations
addition = a + b        # Addition
subtraction = a - b     # Subtraction
multiplication = a * b  # Multiplication
division = a / b        # True division (returns a float)
modulus = a % b         # Modulus (remainder after division)
floor_division = a // b # Floor division (rounds down to nearest whole number)

# Displaying results
print("a =", a, ", b =", b)
print("Addition (a + b):", addition)
print("Subtraction (a - b):", subtraction)
print("Multiplication (a * b):", multiplication)
print("Division (a / b):", division)
print("Modulus (a % b):", modulus)
print("Floor Division (a // b):", floor_division)
